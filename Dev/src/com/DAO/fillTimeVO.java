package com.DAO;

public class fillTimeVO {
	private String time1;
	private String time2;
	private String time3;

	public fillTimeVO(String time1, String time2, String time3) {
		this.time1 = time1;
		this.time2 = time2;
		this.time3 = time3;
	}

	public String getTime1() {
		return time1;
	}

	public String getTime2() {
		return time2;
	}

	public String getTime3() {
		return time3;
	}
	
}
